package array;

public class BubbleSort {
    public static void main(String[] args) {
        int [] arr = {25,36,30,45,28};

        System.out.println("정렬 전");
        for(int a:arr){
            System.out.println(a+" ");
        }
        System.out.println();

        // Selection Sort


        System.out.println("정렬 후");
        for(int a:arr){
            System.out.println(a+" ");
        }
        System.out.println();
    }
}
