package class_.sungjuk;

public class Subject {
    private int kor, eng, math;

    public Subject(int kor, int eng, int math) {
        this.kor = kor;
        this.eng = eng;
        this.math = math;
    }

    public int getKor() {
        return kor;
    }


    public int getEng() {
        return eng;
    }

    public int getMath() {
        return math;
    }

}
